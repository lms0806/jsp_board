package ccomment;

public class Ccomment {
	private int ccommentid;
	private int webid;
	private int commentid;
	private String userid;
	private String ccommentcontent;
	
	public int getCcommentid() {
		return ccommentid;
	}
	public void setCcommentid(int ccommentid) {
		this.ccommentid = ccommentid;
	}
	public int getWebid() {
		return webid;
	}
	public void setWebid(int webid) {
		this.webid = webid;
	}
	public int getCommentid() {
		return commentid;
	}
	public void setCommentid(int commentid) {
		this.commentid = commentid;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getCcommentcontent() {
		return ccommentcontent;
	}
	public void setCcommentcontent(String ccommentcontent) {
		this.ccommentcontent = ccommentcontent;
	}
}
